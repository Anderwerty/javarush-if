package org.example.module1.lesson4;

public class Example4 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        System.out.println("a<b=" + (a < b)); // true
        System.out.println("a>b=" + (a > b));  // false
        System.out.println("a>=b=" + (a >= b)); //false
        System.out.println("a<=b=" + (a <= b)); //true
        System.out.println("a==b=" + (a == b)); //false
        System.out.println("a!=b=" + (a != b)); //true
    }
}
