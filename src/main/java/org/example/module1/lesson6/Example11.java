package org.example.module1.lesson6;

public class Example11 {
    public static void main(String[] args) {
        int i = 0;
        for (; i < 10; ) {
            System.out.println(i++);
        }

        System.out.println("=======================");

        int j = 0;
        while (j < 10) {
            System.out.println(j++);
        }
    }
}
