package org.example.module1.lesson1_2;

public class Example9 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);

        System.out.println(--a);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);

        System.out.println("===============");
        int b = 1;
        System.out.println(-b);
        System.out.println(+b);

    }
}
